package Modelo;

public class Cuenta {
    private long id;
    private long usario;
    private long clave;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getUsario() {
        return usario;
    }

    public void setUsario(long usario) {
        this.usario = usario;
    }

    public long getClave() {
        return clave;
    }

    public void setClave(long clave) {
        this.clave = clave;
    }
}
